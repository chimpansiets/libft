#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;
	int		len;

	ptr = (char*)s;
	len = 0;
	while (*ptr != '\0')
	{
		ptr++;
		len++;
	}
	while (*ptr != c && len > 0)
	{
		ptr--;
		len--;
	}
	if (*ptr == c)
		return (ptr);
	else
		return (NULL);
}
